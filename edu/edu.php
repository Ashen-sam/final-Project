        <h1>Sri Lanka Railways</h1>
        <p>The Sri Lanka Railway Department (more commonly known as Sri Lanka Railways (SLR)) is Sri Lanka's railway owner and primary operator. As part of the Sri Lankan government, it is overseen by the Ministry of Transport. Founded in 1858 as the Ceylon Government Railway, it operates the nation's railways and links Colombo (the capital) with other population centers and tourist destinations.</p>

        <h2>History</h2>
        <p>The construction of a railway in Ceylon was first raised in 1842 by European coffee planters seeking a line be constructed between Kandy and Colombo as a quicker more efficient means to transport their product for export. After protracted negotiations the Ceylon Railway Company was established in 1845, under the chair of Philip Anstruther, Colonial Secretary of Ceylon, to build the colony's first railway. In 1846 the company's engineer, Thomas Drane, undertook preliminary surveys for the new rail line. In December 1856 Captain William Scarth Moorsom, Chief Engineer of the Corps of Royal Engineers, was sent from England to assess the project for the Secretary of State for the Colonies, Henry Labouchere. His report, issued May 1857, considered six alternative routes to Kandy and recommended the adoption of Route No.3 via the Parnepettia Pass, with a total length of 127 km (79 mi), a ruling gradient of one in 60, with a short Tunnel at an estimated cost of £856,557. The initial sod turning was on 3 August 1858 (near the present Maradana railway station) by Governor Sir Henry Ward. The Ceylon Railway Company's contractor, William Thomas Doyne, soon realized that it was impossible to complete the work on the estimate submitted. In 1861, the contract with the Ceylon Railway Company was terminated, the subscribed capital paid off, and the government took over the construction work, under the name Ceylon Government Railway (now Sri Lanka Railway). At the end of 1862 the Crown Agents for the Colonies accepted, on behalf of the Government of Ceylon, a tender from William Frederick Faviell for the construction of 117 km (73 mi) of railway between Colombo and Kandy.</p>
