        <h1>Sri Lanka Railways</h1>

        <h2>Network</h2>
        <p>The 1,508-kilometre (937 mi) Sri Lankan railway network is 5 ft 6 in (1,676 mm) broad gauge. All service is diesel-powered. The network is divided into three operating regions based in Colombo, Anuradhapura, and Nawalapitiya. The railway is modernizing and extending the Coastal Line for faster trains and improved efficiency.</p>

        <h2>Electrification</h2>
        <p>Although electrification was first proposed in 1928, the cabinet did not approve the electrification of suburban railways until 2015. Electrification of the Panadura-Veyangoda line is proposed in phase one of the Western Region Megapolis plan with a soft loan from the Asian Development Bank. A contract was signed by Malaysia's Airport Express Air and Rail Company and the government of Sri Lanka for a new electric rail line between Negombo and Colombo, and the project was expected to be completed by 2018. Electrification of the busiest sections of the network was proposed several times to improve energy efficiency and sustainability. Around 1998, the Institution of Engineers, Sri Lanka (IESL) submitted recommendations for railway electrification. Although they were approved by the cabinet, they were not implemented. The IESL made new proposals for electrification in 2008 and 2010, but no work was done because the voltage systems were undefined. System electrification is favored by the IESL to reduce pollution and travel time and increase passenger comfort. Although Sri Lanka Railways is planning to electrify the 120-kilometre (75 mi) Colombo commuter-rail system from Veyangoda to Maradana, Maradana to Kaluthara and Ragama to Negombo, their voltage systems are unknown. Fifteen electric multiple units will be imported for commuter service.</p>

        <h2>Signalling</h2>
        <p>Much of the network uses a lock-and-block signaling system. During the mid-twentieth century, the busiest sectors (around Colombo) were upgraded to electronic signaling connected to a CTC control panel at the Maradana railway station. In 2011, a project to add electronic signaling to the northern lines began. Track between Anuradhapura, Kankesanturai, and Talaimannar would have electronic signaling with centralized traffic control: an interlocking color-light system with electrically operated points and a track-detection system. Level crossings would be connected to the signaling system, ensuring safety. After the 2011 Alawwa rail accident, SLR began installing a GPS-based train-protection system on its fleet. The system warns a train driver of a possible collision in time to manually stop the train. The fleet can also be monitored by a central control room with the system. A trial run with ten trains was conducted in early November 2011.</p>

        <h2>Routes</h2>
        <p>Major population centers and tourist destinations are connected by rail. Service began in 1864 with the construction of the Main Line from Colombo to Ambepussa, 54 kilometers (34 mi) east, and the first train ran on 27 December 1864. The line was officially opened to traffic on 2 October 1865. The Main Line was extended in stages, with service to Kandy in 1867, to Nawalapitiya in 1874, to Nanu-Oya in 1885, to Bandarawela in 1894, and to Badulla in 1924. Other lines were completed to link the country: the Matale Line in 1880, the Coastal Line in 1895, the Northern Line in 1905, the Mannar Line in 1914, the Kelani Valley Line in 1919, the Puttalam Line in 1926, and the Batticaloa and Trincomalee Lines in 1928.</p>

